function normalizeCatchphraseText(text: string) {
  return text
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase();
}

export function countCattyCatchphrases(text: string) {
  const normalized = normalizeCatchphraseText(text);

  return CATTY_SIGNATURE_EXPRESSIONS.filter((expression) =>
    normalized.includes(normalizeCatchphraseText(expression)),
  ).length;
}

export function hasTooManyCattyCatchphrases(text: string) {
  return countCattyCatchphrases(text) > 1;
}
